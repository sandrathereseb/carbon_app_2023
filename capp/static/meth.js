var voksen = document.getElementById("kek");
const drop1 = document.getElementById('one');
drop1.addEventListener('click', (e) => {
    dropin1()
})
function dropin1() {
    if (voksen.style.display === "none") {
        voksen.style.display = "block";
    } else {
        voksen.style.display = "none"
    }
}

